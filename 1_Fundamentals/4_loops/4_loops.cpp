#include<iostream>
using namespace std;

int main() {
    
    int i;

    //1. for loop
    for (i= 0; i< 10; i++) {
        cout << i << " ";
    }
    
    //2. while loop
    i= 0;
    while (i < 10) {
        cout << i << " ";
    }

    //3. do-while loop
    do {
        cout << i << " ";
    }while(i<10);


return 0;
}
   
   
